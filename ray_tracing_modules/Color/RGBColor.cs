namespace ray_tracing_modules.Color
{
    public class RGBColor
    {
        public double R { get; set; }
        public double G { get; set; }
        public double B { get; set; }

        public RGBColor(double r, double g, double b)
        {
            R = r;
            G = g;
            B = b;
        }
    }
}
